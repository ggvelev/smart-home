package com.iot.smarthome.dto;

import com.fasterxml.jackson.annotation.JsonProperty;
import com.iot.smarthome.entity.DevicePropertyType;

public class DeviceCommand {

    private final DevicePropertyType type;
    private final String value;

    public DeviceCommand(@JsonProperty("type") DevicePropertyType type,
                         @JsonProperty("value") String value) {
        this.type = type;
        this.value = value;
    }

    public DevicePropertyType getType() {
        return type;
    }

    public String getValue() {
        return value;
    }

    @Override
    public boolean equals(Object o) {
        if (this == o) {
            return true;
        }
        if (o == null || getClass() != o.getClass()) {
            return false;
        }

        DeviceCommand that = (DeviceCommand) o;

        if (type != that.type) {
            return false;
        }
        return value != null ? value.equals(that.value) : that.value == null;
    }

    @Override
    public int hashCode() {
        int result = type != null ? type.hashCode() : 0;
        result = 31 * result + (value != null ? value.hashCode() : 0);
        return result;
    }

    @Override
    public String toString() {
        return "DeviceCommand{" +
                "type=" + type +
                ", value='" + value + '\'' +
                '}';
    }
}